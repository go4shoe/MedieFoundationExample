		memcpy (pRawBuffer, &startCode, 4);

		offset += 4;

		memcpy (pRawBuffer + offset, &sequenceParameters, sizeof(sequenceParameters));

		offset += sizeof (sequenceParameters);

		memcpy (pRawBuffer + offset, &startCode, 4);

		offset += 4;

		memcpy (pRawBuffer + offset, &pictureParameters, sizeof(pictureParameters));


