	ComPtr<IMFTransform> pH264Decoder = nullptr;

	CheckResult (pUnknown->QueryInterface(IID_PPV_ARGS(&pH264Decoder)));


	ComPtr<IMFMediaType> pInputMediaType = nullptr;

	CheckResult (MFCreateMediaType(&pInputMediaType));
	
	CheckResult (pInputMediaType->SetGUID(MF_MT_MAJOR_TYPE, MFMediaType_Video));
	
