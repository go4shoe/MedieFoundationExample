	CheckResult (pH264Decoder->ProcessMessage(MFT_MESSAGE_NOTIFY_START_OF_STREAM, NULL));

	const uint32_t startCode = 0x01000000;

	std::ofstream out("stream.h264", std::ios::out | std::ios::binary);

	// Send sequence and picture parameters
