	CheckResult (MFSetAttributeRatio(pOutputType.Get(), MF_MT_PIXEL_ASPECT_RATIO, width, height));
	
	CheckResult (MFSetAttributeRatio(pOutputType.Get(), MF_MT_FRAME_RATE, fps, 1));

	CheckResult (pH264Decoder->SetOutputType(0, pOutputType.Get(), 0));

	// Notify the resampler.

	CheckResult (pH264Decoder->ProcessMessage(MFT_MESSAGE_COMMAND_FLUSH, NULL));

